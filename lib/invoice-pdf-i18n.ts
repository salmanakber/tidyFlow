function normalizeLocale(lang?: string | null): InvoicePdfLocale {
  const code = (lang || 'en').toLowerCase().split('-')[0];
  if (code in CLIENT) return code as InvoicePdfLocale;
  return 'en';
}

export function getClientInvoicePdfLabels(lang?: string | null): ClientInvoicePdfLabels {
  return CLIENT[normalizeLocale(lang)];
}

export function getPayslipPdfLabels(lang?: string | null): PayslipPdfLabels {
  return PAYSLIP[normalizeLocale(lang)];
}

export function getPayslipLineLabels(lang?: string | null): PayslipLineLabels {
  return PAYSLIP_LINES[normalizeLocale(lang)];
}

export function formatPayslipLine(
  template: string,
  vars: Record<string, string | number>
): string {
  return Object.entries(vars).reduce(
    (result, [key, value]) => result.replaceAll(`{{${key}}}`, String(value)),
    template
  );
}

export function invoiceDateLocale(lang?: string | null): string {
  const code = normalizeLocale(lang);
  const map: Record<InvoicePdfLocale, string> = {
    en: 'en-GB',
    es: 'es-ES',
    pt: 'pt-PT',
    fr: 'fr-FR',
    de: 'de-DE',
    it: 'it-IT',
    pl: 'pl-PL',
    no: 'nb-NO',
    sv: 'sv-SE',
    ar: 'ar-SA',
    cn: 'zh-CN',
  };
  return map[code];
}
